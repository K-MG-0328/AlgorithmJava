package 나의풀이;

import java.util.Scanner;

public class 직사각형별찍기 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        String s = "************************************************************************************************************" +
                "****************************************************************************************************************" +
                "*****************************************************************************************************************" +
                "******************************************************************************************************************" +
                "*******************************************************************************************************************" +
                "********************************************************************************************************************" +
                "*********************************************************************************************************************" +
                "**********************************************************************************************************************" +
                "***************************************************************************************";

        StringBuilder sb = new StringBuilder();
        sb.append(s, 0, a);
        for (int i = 0; i < b; i++) {
            System.out.println(sb.toString());
        }
    }
}
